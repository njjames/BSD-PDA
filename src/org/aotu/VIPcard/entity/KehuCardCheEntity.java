package org.aotu.VIPcard.entity;

import org.nutz.dao.entity.annotation.Column;
import org.nutz.dao.entity.annotation.Id;
import org.nutz.dao.entity.annotation.Table;

/**
 * @author 刘宏德
 *
 */
@Table(value="kehu_card_che")
public class KehuCardCheEntity {
	/**
	 * 
	 */
	@Id
	@Column
	private Integer reco_no;
	/**
	 * 
	 */
	@Column
	private String card_no;
	/**
	 * 
	 */
	@Column
	private String che_no;
	/**
	 * 
	 */
	@Column
	private String che_py;
	/**
	 * 
	 */
	@Column
	private String che_cx;
	/**
	 * 
	 */
	@Column
	private String che_wxys;
	public Integer getReco_no() {
		return reco_no;
	}
	public void setReco_no(Integer reco_no) {
		this.reco_no = reco_no;
	}
	public String getCard_no() {
		return card_no;
	}
	public void setCard_no(String card_no) {
		this.card_no = card_no;
	}
	public String getChe_no() {
		return che_no;
	}
	public void setChe_no(String che_no) {
		this.che_no = che_no;
	}
	public String getChe_py() {
		return che_py;
	}
	public void setChe_py(String che_py) {
		this.che_py = che_py;
	}
	public String getChe_cx() {
		return che_cx;
	}
	public void setChe_cx(String che_cx) {
		this.che_cx = che_cx;
	}
	public String getChe_wxys() {
		return che_wxys;
	}
	public void setChe_wxys(String che_wxys) {
		this.che_wxys = che_wxys;
	}
	
}
