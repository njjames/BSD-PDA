package org.aotu.user.entity;

import org.nutz.dao.entity.annotation.Column;
import org.nutz.dao.entity.annotation.Id;
import org.nutz.dao.entity.annotation.Table;

@Table("sm_caozuoyuan")
public class userEntity {
	
	@Id
	@Column
	int id;
	
	@Column("caozuoyuan_xm")
	private String name;
	
	@Column("caozuoyuan_password")
	private String psd;
	
	@Column
	private String caozuoyuan_Gndm; 
	
	@Column
	private String gongsi_gndm;
	
	@Column
	private String cangku_gndm;
	
	@Column
	private String cangkudo_gndm;
	
	@Column
	private int xiao_yhlv;
	
	@Column
	private String GongSiNo;
	
	@Column
	private String GongSiMc;
	
	
	/**
	 * 公司电话
	 */
	private String danw_dh;
	
	/**
	 * 公司手机
	 */
	private String danw_shouji;
	
	/**
	 * 公司地址
	 */
	private String danw_dz;

	public int getId() {
		return id;
	} 

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPsd() {
		return psd;
	}

	public void setPsd(String psd) {
		this.psd = psd;
	}

	public String getCaozuoyuan_Gndm() {
		return caozuoyuan_Gndm;
	}

	public void setCaozuoyuan_Gndm(String caozuoyuan_Gndm) {
		this.caozuoyuan_Gndm = caozuoyuan_Gndm;
	}

	public String getGongsi_gndm() {
		return gongsi_gndm;
	}

	public void setGongsi_gndm(String gongsi_gndm) {
		this.gongsi_gndm = gongsi_gndm;
	}

	public String getCangku_gndm() {
		return cangku_gndm;
	}

	public void setCangku_gndm(String cangku_gndm) {
		this.cangku_gndm = cangku_gndm;
	}

	public String getCangkudo_gndm() {
		return cangkudo_gndm;
	}

	public void setCangkudo_gndm(String cangkudo_gndm) {
		this.cangkudo_gndm = cangkudo_gndm;
	}

	public int getXiao_yhlv() {
		return xiao_yhlv;
	}

	public void setXiao_yhlv(int xiao_yhlv) {
		this.xiao_yhlv = xiao_yhlv;
	}

	public String getGongSiNo() {
		return GongSiNo;
	}

	public void setGongSiNo(String gongSiNo) {
		GongSiNo = gongSiNo;
	}

	public String getGongSiMc() {
		return GongSiMc;
	}

	public void setGongSiMc(String gongSiMc) {
		GongSiMc = gongSiMc;
	}

	public String getDanw_dh() {
		return danw_dh;
	}

	public void setDanw_dh(String danw_dh) {
		this.danw_dh = danw_dh;
	}

	public String getDanw_shouji() { 
		return danw_shouji;
	}

	public void setDanw_shouji(String danw_shouji) {
		this.danw_shouji = danw_shouji;
	}

	public String getDanw_dz() {
		return danw_dz;
	}

	public void setDanw_dz(String danw_dz) {
		this.danw_dz = danw_dz;
	}
	
}
